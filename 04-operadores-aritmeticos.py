# --> Suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

print(suma)
print(resta)

# --> Multiplicacion y division (* y /)

multi = 12 * 5
division = 12 / 5  # Esta division da como resultado un dato float.

print(multi)
print(division)

# --> Potenciacion o exponente (**)
exponente = 12**5

print(exponente)
# --> Division baja (//)
division_baja = 12 // 5  # Devuelve un entero redondeado hacia abajo.

print(division_baja)
# --> Resto o modulo
resto = 12 % 5

print(resto)

# --> Tipo de dato
tipo_de_dato = type(5)  # nos devuelve el tipo de dato.

print(tipo_de_dato)
